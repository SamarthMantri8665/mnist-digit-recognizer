folder_name = 'mnist_sample_images'
os.makedirs(folder_name, exist_ok=True)

test_dataset = datasets.MNIST(root='./data', train=False, download=True)

saved_digits = set()
idx = 0

print("Extracting images...")

while len(saved_digits) < 10 and idx < len(test_dataset):
    image, label = test_dataset[idx]

    if label not in saved_digits:
        file_path = os.path.join(folder_name, f"actual_digit_{label}.png")
        image.save(file_path)
        saved_digits.add(label)
        print(f"✅ Saved a '{label}' to {file_path}")

    idx += 1

print("\nDone! Check the 'mnist_sample_images' folder.")